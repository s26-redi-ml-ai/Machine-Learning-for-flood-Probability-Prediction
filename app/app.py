# ── Risk tier ──────────────────────────────────────────────────────────────────
def risk_tier(p):
    if p < 0.40: return "Low",       "#1D9E75", "🟢", "#EAF4EF"
    if p < 0.55: return "Moderate",  "#BA7517", "🟡", "#FEF5E7"
    if p < 0.68: return "High",      "#D85A30", "🟠", "#FDECEA"
    return              "Very High", "#8B0000", "🔴", "#F9D6D5"

# ── Premium multiplier (simplified actuarial framing) ─────────────────────────
def premium_multiplier(p):
    # illustrative non-linear scaling used in cat models
    base = 1.0
    if p < 0.40: return base * (1 + p * 0.5)
    if p < 0.55: return base * (1 + p * 1.2)
    if p < 0.68: return base * (1 + p * 2.0)
    return base * (1 + p * 3.5)

# ── SHAP waterfall (matplotlib) ────────────────────────────────────────────────
def plot_waterfall(sv, feature_df, n=12):
    vals  = sv.values[0]
    base  = sv.base_values[0]
    names = feature_df.columns.tolist()
    fvals = feature_df.iloc[0].values

    pairs = sorted(zip(np.abs(vals), vals, names, fvals), reverse=True)[:n]
    _, sv_s, fn_s, fv_s = zip(*pairs)
    sv_s = list(reversed(sv_s)); fn_s = list(reversed(fn_s)); fv_s = list(reversed(fv_s))

    fig, ax = plt.subplots(figsize=(8, 4.5))
    fig.patch.set_facecolor("white")
    ax.set_facecolor("white")

    cols = [TEAL if v >= 0 else CORAL for v in sv_s]
    ax.barh(range(n), sv_s, color=cols, alpha=0.85, height=0.55)
    for i,(v,nm,fv) in enumerate(zip(sv_s,fn_s,fv_s)):
        lbl = f"{nm} = {fv:.0f}"
        ax.text(-0.0005 if v<0 else 0.0005, i, lbl,
                va="center", ha="right" if v<0 else "left",
                fontsize=8.5, color="#222")
    ax.axvline(0, color="#444", lw=0.8)
    ax.set_yticks([]); ax.set_xlabel("SHAP value", fontsize=9)
    ax.set_title(f"Prediction breakdown  (baseline = {base:.3f})", fontsize=10, pad=10)
    ax.spines[["top","right","left"]].set_visible(False)
    ax.grid(axis="x", alpha=0.2)
    pos = mpatches.Patch(color=TEAL,  label="↑ Increases risk")
    neg = mpatches.Patch(color=CORAL, label="↓ Decreases risk")
    ax.legend(handles=[pos,neg], fontsize=8, loc="lower right")
    plt.tight_layout()
    return fig

# ── Plotly gauge ───────────────────────────────────────────────────────────────
def gauge_chart(prob, tier_color):
    fig = go.Figure(go.Indicator(
        mode="gauge+number",
        value=round(prob*100, 1),
        number={"suffix": "%", "font": {"size": 36, "color": tier_color}},
        gauge={
            "axis": {"range": [0,100], "tickwidth": 1, "tickcolor": "#888"},
            "bar":  {"color": tier_color, "thickness": 0.25},
            "steps": [
                {"range": [0, 40],  "color": "#E1F5EE"},
                {"range": [40, 55], "color": "#FEF5E7"},
                {"range": [55, 68], "color": "#FDECEA"},
                {"range": [68, 100],"color": "#F9D6D5"},
            ],
            "threshold": {"line": {"color": tier_color, "width": 3}, "value": prob*100},
        },
        domain={"x":[0,1],"y":[0,1]}
    ))
    fig.update_layout(
        height=220, margin=dict(l=20,r=20,t=30,b=10),
        paper_bgcolor="white", font=dict(family="Arial")
    )
    return fig

# ── Domain bar chart (Plotly) ──────────────────────────────────────────────────
def domain_bars(raw):
    nat  = np.mean([raw[f] for f in NATURAL_FEATURES])
    infr = np.mean([raw[f] for f in INFRA_FEATURES])
    soc  = np.mean([raw[f] for f in SOCIO_FEATURES])
    fig = go.Figure(go.Bar(
        x=["Natural /\nClimate", "Human /\nInfra", "Socio-\npolitical"],
        y=[nat, infr, soc],
        marker_color=[BLUE, TEAL, CORAL],
        text=[f"{v:.1f}/15" for v in [nat, infr, soc]],
        textposition="outside",
    ))
    fig.update_layout(
        height=200, margin=dict(l=10,r=10,t=30,b=10),
        yaxis_range=[0, 16],
        title=dict(text="Domain risk scores", font=dict(size=13)),
        paper_bgcolor="white", plot_bgcolor="white",
        yaxis=dict(showgrid=True, gridcolor="#EEE"),
        xaxis=dict(showgrid=False),
        showlegend=False,
        font=dict(family="Arial", size=11)
    )
    return fig

# ── Scenario radar (Plotly) ────────────────────────────────────────────────────
def radar_chart(raw):
    cats = ["Natural", "Infrastructure", "Socio-political", "Natural"]
    vals = [
        np.mean([raw[f] for f in NATURAL_FEATURES]),
        np.mean([raw[f] for f in INFRA_FEATURES]),
        np.mean([raw[f] for f in SOCIO_FEATURES]),
        np.mean([raw[f] for f in NATURAL_FEATURES]),
    ]
    fig = go.Figure(go.Scatterpolar(
        r=vals, theta=cats, fill="toself",
        fillcolor=TEAL, opacity=0.25,
        line=dict(color=TEAL, width=2)
    ))
    fig.update_layout(
        polar=dict(radialaxis=dict(visible=True, range=[0,15])),
        height=250, margin=dict(l=30,r=30,t=40,b=10),
        title=dict(text="Risk profile radar", font=dict(size=13)),
        paper_bgcolor="white", font=dict(family="Arial", size=11)
    )
    return fig

# ── Insurance interpretation ───────────────────────────────────────────────────
def insurance_text(prob, tier, nat_avg, infr_avg, soc_avg):
    mult = premium_multiplier(prob)
    lines = []
    if tier == "Low":
        lines.append(f"**Underwriting decision:** Standard policy, no referral required.")
        lines.append(f"**Premium loading:** ~{(mult-1)*100:.0f}% above base rate (illustrative).")
        lines.append(f"**Key driver:** All three risk domains are below average. No mitigation required.")
    elif tier == "Moderate":
        lines.append(f"**Underwriting decision:** Standard policy with flood excess clause recommended.")
        lines.append(f"**Premium loading:** ~{(mult-1)*100:.0f}% above base rate (illustrative).")
        dominant = "infrastructure" if infr_avg >= max(nat_avg, soc_avg) else ("natural" if nat_avg >= soc_avg else "socio-political")
        lines.append(f"**Dominant risk domain:** {dominant.title()} factors are driving elevated risk.")
        lines.append(f"**Mitigation note:** Drainage improvements or land-use restrictions could reduce score.")
    elif tier == "High":
        lines.append(f"**Underwriting decision:** Refer to senior underwriter. Flood sub-limit likely required.")
        lines.append(f"**Premium loading:** ~{(mult-1)*100:.0f}% above base rate (illustrative).")
        lines.append(f"**Mitigation note:** Location requires flood survey before binding. Consider exclusion clause.")
    else:
        lines.append(f"**Underwriting decision:** 🚨 Decline or specialist flood market only.")
        lines.append(f"**Premium loading:** ~{(mult-1)*100:.0f}% above base rate — likely uninsurable at standard terms.")
        lines.append(f"**Risk note:** Multiple compounding factors. Requires catastrophe model review.")
    return lines

